#pragma once

namespace Tmk
{
    /// <summary>
    /// Contains the terminal font weights.
    /// </summary>
    enum class FontWeight
    {
        /// <summary>
        /// Usually rendered with bold weight and/or with light colors.
        /// </summary>
        Bold = 1,
        /// <summary>
        /// Usually rendered with dimmed colors.
        /// </summary>
        Light
    };
}
